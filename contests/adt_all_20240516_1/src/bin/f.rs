use std::vec;

use itertools::Itertools;
use proconio::input;

fn solve() {
    input! {
        n: usize
    }
    let mut k = n - 1;
    let mut ans = vec![];


    while(k != 0) {
        ans.push(k % 5);
        k /= 5;
    }
    ans.reverse();

    if ans.is_empty() {
        ans.push(0);
    }

    println!("{}", ans.iter().map(|&x| x * 2).join(""));
}

