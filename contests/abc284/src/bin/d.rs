fn solve() {
    input!{
        n: usize,        
    }


    for x in 2.. {
        if n % x == 0 {
            let k = n / x;
            if k % x == 0 {
                println!("{} {}", x, k / x);
            } else {
                println!("{} {}", k.sqrt(), x);
            }
            return;
        }
    }
}


