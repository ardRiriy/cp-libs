use proconio::input;

fn solve() {
    input! {
        n: usize,
        m: usize,
        h: [u64; n]
    }

    let mut k = m as u64;
    let mut ans = 0;
    for &i in &h {
        if k >= i {
            k -= i;
            ans += 1;
        } else {
            break;
        }
    }
    println!("{}", ans);
}

