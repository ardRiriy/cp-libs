use proconio::{input};
fn solve() {
    input!{
        n: usize,
        x: u64,
        y: u64,
        mut a: [u64; n],
        mut b: [u64; n]
    }

    a.sort();
    b.sort();
    a.reverse();
    b.reverse();
    let mut a_s = 0;
    let mut b_s = 0;

    for i in 0..n {
        a_s += a[i];
        b_s += b[i];
        if a_s > x || b_s > y {
            println!("{}", i + 1);
            return;
        }
    }

    println!("{}", n);
}

