use proconio::{input};
fn solve() {
    input!{
        n: usize,
        a: [u64; n],
        w: [u64; n]
    }

    let mut v = vec![(0, 0); n];

    for i in 0..n {
        v[a[i] as usize - 1].0 += w[i];
        v[a[i] as usize - 1].1.chmax(w[i]);
    }

    let ans = v.iter().map(|&(sum, max)| sum - max).sum::<u64>();
    println!("{}", ans);
}

