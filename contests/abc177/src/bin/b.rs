use proconio::input;
use proconio::marker::Chars;

fn solve() {
    input! {
        s: Chars,
        t: Chars,
    }

    let mut ans = INF;
    for i in 0..=s.len() - t.len() {
        let mut diff = 0;
        for j in 0..t.len() {
            if s[i + j] != t[j] {
                diff += 1;
            }
        }
        ans.chmin(diff);
    }
    println!("{ans}");

}

