use std::mem::swap;

use proconio::input;

fn solve() {
    input! {
        mut a: u64,
        b: u64,
        mut c: u64,
    }

    if a > c {
        swap(&mut a, &mut c);
    }

    if a <= b && b <= c {
        println!("Yes");
    } else {
        println!("No");
    }
}

