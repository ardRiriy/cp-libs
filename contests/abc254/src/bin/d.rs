use proconio::input;

fn solve() {
    input! {
        n: usize
    }

    let mut ans = 0u64;
    for i in 1..=n {
        let mut k = i;
        let mut j = 2;
        while j * j <= n {
            while k % (j * j) == 0 {
                k /= j * j;
            }
            j += 1;
        }
        let mut d = 1;
        while k * d * d <= n {
            ans += 1;
            d += 1;
        }
    }
    println!("{}", ans);


}


