fn solve() {
    input! {
        h: usize,
    }
    let mut i = 0;
    let mut k = 1;

    for j in 0usize.. {
        i += k;
        k *= 2;
        if i > h {
            println!("{}", j + 1);
            return;
        }
    }
}

