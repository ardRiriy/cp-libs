use proconio::{input, marker::Chars};

fn solve() {
    input! {
        n: usize,
        m: usize,
        s: Chars
    }

    let mut use_nomal = 0;
    let mut lack = 0;
    let mut ans = 0;

    for &c in s.iter() {
        if c == '0' {
            ans.chmax(lack);
            lack = 0;
            use_nomal = 0;
        } else if c == '1' {
            if use_nomal == m {
                lack += 1;
            } else {
                use_nomal += 1;
            }
        } else {
            lack += 1;
        }
    }

    ans.chmax(lack);
    println!("{}", ans);
}

