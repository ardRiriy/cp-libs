use std::collections::BTreeSet;

use proconio::input;

fn solve() {
    input! {
        a: usize,
        b: usize,
    }

    if a == b {
        println!("{}", -1);
    } else {
        let mut set = BTreeSet::from([1, 2, 3]);
        set.remove(&a);
        set.remove(&b);
        println!("{}", *set.iter().min().unwrap());
    }
}

