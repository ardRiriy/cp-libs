use ac_library::ModInt998244353;
use proconio::{input, marker::Chars};
fn main() {
    input!{
        mut t: i32,
    }

    for _ in 0..t {
        solve();
    }
}

fn solve() {
    input! {
        n: usize,
        s: Chars,
    }

    let mut ans = ModInt998244353::new(0);
    let base =  ModInt998244353::new(26);

    let check_len = n / 2 + n % 2;

    for i in 0..check_len {
        let p = s[i] as u64 - 'A' as u64;
        ans += base.pow(check_len as u64 - (i + 1) as u64) * p;
    }

    let mut flg = true;
    for i in (0..check_len).rev() {
        let rev_i = n - i - 1;
        if (s[i] as u8) < s[rev_i] as u8{
            ans += 1;
            flg = false;
            break;
        } else if (s[i] as u8) > s[rev_i] as u8 {
            flg = false;
            break;
        }
    }

    if flg {
        ans += 1;
    }

    println!("{}", ans);
}

