use ac_library::Dsu;
use proconio::{input, marker::Usize1};
fn solve() {
    input!{
        n: usize,
        m: usize,
    }

    let mut dsu = (0..m).fold(Dsu::new(n), |mut acc, _| {
        input! {
            a: Usize1,
            b: Usize1
        }
        acc.merge(a, b);
        acc
    });

    let mut seen = vec![false; n];
    let need = (0..n).map(|i| 
        if seen[dsu.leader(i)] {
            0
        } else {
            seen[dsu.leader(i)] = true;
            dsu.size(i) - 1
        }
    ).sum::<usize>();
    
    println!("{}", m - need);
    

}

